    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
          boolean matched = false;
        String usn = txtUser.getText().toString();  
        String pss = txtPassword.getText().toString();

        try {
            FileReader aa = new FileReader("Acc.txt");
            BufferedReader br = new BufferedReader(aa);
            String line;
            String str;
            while((str=br.readLine())!=null){
                if(str.equals(usn+"\t"+pss)){
                    matched = true;
                    break;
                }            
            }
            aa.close();
            if(matched){
                UserProfile mF = new UserProfile();
                mF.setVisible(true);
                this.dispose();  
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

